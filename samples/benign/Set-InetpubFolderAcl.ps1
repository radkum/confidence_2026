<#
.SYNOPSIS
Set security permissions on the directory inetpub.

.DESCRIPTION
This script sets the security permissions on the directory inetpub. The script is designed to be run as an administrator.
If inetpub directory does not exist, it gets created and default IIS permissions are applied to the directory.
If an empty inetpub directory exists, the default IIS permissions are applied to the directory.
If inetpub directory exists and only contains the sub-directory DeviceHealthAttestation, default IIS permissions are applied to both directories.
If inetpub directory exists and contains any other sub-directories, the script exits without making any changes.

.PARAMETER WhatIf
Preview changes the script will make without making the changes.

.EXAMPLE
.\Set-InetpubFolderAcl.ps1 -whatif

.EXAMPLE
.\Set-InetpubFolderAcl.ps1
#>

[CmdletBinding(SupportsShouldProcess=$true)]
Param()

function script:Test-HasUserData
{
    if (-not (Test-Path -Path $inetpubPath))
    {
        return $False
    }

    $subFolders = Get-ChildItem -Path $inetpubPath | ? { $_.PSIsContainer -and !($_.Attributes -match "ReparsePoint") }

    #
    # win7: gci returns an object instead of collection if the directory contains only 1 sub-directory.
    # Count property is null in this case since the return value is not a collection.
    #
    if ($null -ne $subfolders -and $null -eq $subfolders.Count -and $subfolders.Name -ne $dhaFolder)
    {
        return $True
    }

    #
    # Check if inetpub directory has any sub-directories in it other than DeviceHealthAttestation.
    #
    if ($subFolders.Count -gt 1 -or ($subFolders.Count -eq 1 -and $subFolders[0].Name -ne $dhaFolder))
    {
        return $True
    }

    return $False
}

function script:Set-FolderAcl
{
    param (
        [Parameter(Mandatory=$true)]
        [String] $folderPath,

        [Parameter(Mandatory=$true)]
        [String] $sddl,

        [Parameter(Mandatory=$false)]
        [switch] $Quiet
    )

    if (Test-Path $folderPath)
    {
        $folder = Get-Item $folderPath -ErrorAction SilentlyContinue;
        if (($folder.Attributes -band [System.IO.FileAttributes]::ReparsePoint) -ne 0)
        {
            Write-Output "${folderPath} is a reparse point and will be ignored."
            return
        }
    }
    else
    {
        Write-Output "${folderPath} not found."
        return
    }

    # Print current ACLs
    if (-not $Quiet)
    {
        Write-Output "Current ACL on ${folderPath}:"
        Get-Acl $folderPath | format-list
    }

    # Create the ACL object using the SDDL string
    ($acl = New-Object -TypeName System.Security.AccessControl.DirectorySecurity).SetSecurityDescriptorSddlForm($sddl)

    try
    {
        Set-Acl -Path $folderPath -AclObject $acl -errorAction stop
    }
    catch
    {
        #
        # win7: Set-ACL fails for ACLs with SYSTEM as owner. Replace SYSTEM with BA as the owner and retry
        #
        if ($_.Exception -is [System.InvalidOperationException])
        {
            Write-Output "Set-Acl failed with invalid operation exception. Retrying with built-in administrator as owner."
            $sddlNew = $sddl -replace 'O:.+G:.+(D:.*)', 'O:BAG:BA$1'
            ($acl = New-Object -TypeName System.Security.AccessControl.DirectorySecurity).SetSecurityDescriptorSddlForm($sddlNew)
            Set-Acl -Path $folderPath -AclObject $acl -ErrorAction Continue
        }
        else
        {
            Write-Output $_.Exception
        }
    }

    # Print new ACLs
    if (-not $Quiet)
    {
        Write-Output "New ACL on ${folderPath}:"
        Get-Acl $folderPath | format-list
    }
}

function script:Set-InetpubAcl
{
    [CmdletBinding(SupportsShouldProcess = $true)]
    param ()

    # Set ACLs on the inetpub directory
    if ($PSCmdlet.ShouldProcess($inetpubPath, "Set default IIS security permissions"))
    {
        Set-FolderAcl -folderPath $inetpubPath -sddl $sddlInetpub -Quiet
    }

    # Set ACLs on DeviceHealthAttestation and DeviceHealthAttestation/bin sub-directories
    if (Test-Path -Path $dhaPath)
    {
        if ($PSCmdlet.ShouldProcess($dhaPath, "Set default IIS security permissions"))
        {
            Set-FolderAcl -folderPath $dhaPath -sddl $sddlInetpub
        }
    }
    if (Test-Path -Path $dhabinPath)
    {
        if ($PSCmdlet.ShouldProcess($dhabinPath, "Set default IIS security permissions"))
        {
            Set-FolderAcl -folderPath $dhabinPath -sddl $sddlInetpub
        }
    }
}

