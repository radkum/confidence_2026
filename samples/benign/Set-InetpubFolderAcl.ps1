# Ensure script is running as administrator
$IsAdmin = ([Security.Principal.WindowsPrincipal][Security.Principal.WindowsIdentity]::GetCurrent()).IsInRole([Security.Principal.WindowsBuiltInRole]"Administrator")
if (-not $IsAdmin)
{
    Write-Output "The script needs admin rights to run and needs to be launched from an elevated command prompt."
    Exit
}

$systemDrive = $env:SystemDrive
$inetpubFldr = "inetpub"
$inetpubPath = Join-Path -Path $systemDrive\ -ChildPath $inetpubFldr
$dhaFolder = "DeviceHealthAttestation"
$dhaPath = Join-Path -Path $inetpubPath\ -ChildPath $dhaFolder
$dhabinPath = Join-Path -Path $dhaPath\ -ChildPath 'bin'
$sddlInetpub = "O:SYG:SYD:P(A;CIOI;GA;;;S-1-5-80-956008885-3418522649-1831038044-1853292631-2271478464)(A;CIOI;GA;;;SY)(A;CIOI;GA;;;BA)(A;CIOI;GRGX;;;BU)(A;CIOI;GA;;;CO)"

if (Test-Path -Path $inetpubPath)
{
    $folder = Get-Item -Path $inetpubPath
    if (($folder.Attributes -band [System.IO.FileAttributes]::ReparsePoint) -ne 0)
    {
        Write-Output "This script does not support the inetpub directory that is a reparse point."
        Exit
    }

    if (Test-HasUserData $inetpubPath)
    {
        Write-Output "The inetpub directory is not empty. No action will be taken."
        Exit
    }

    $oldInetpubAcl = Get-Acl $inetpubPath
}
else
{
    if ($PSCmdlet.ShouldProcess($inetpubPath, "Create directory"))
    {
        New-Item -Path $systemDrive\ -Name $inetpubFldr -Type "Directory" -ErrorAction Stop | Out-Null
    }
}

Set-InetpubAcl

# Print ACLs on inetpub
if (-not $WhatIfPreference)
{
    if ($null -ne $oldInetpubAcl)
    {
        Write-Output "Current security permissions on ${inetpubPath}:"
        $oldInetpubAcl | format-list
    }
    if (Test-Path -Path $inetpubPath)
    {
        Write-Output "New security permissions on ${inetpubPath}:"
        Get-Acl $inetpubPath | format-list
    }
}
